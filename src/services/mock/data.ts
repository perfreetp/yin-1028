const generateSensorData = (): SensorData[] => {
  const data: SensorData[] = [];
  const now = new Date();
  for (let i = 24; i >= 0; i--) {
    const timestamp = new Date(now.getTime() - i * 60 * 60 * 1000).toISOString();
    data.push({
      timestamp,
      moisture: 15 + Math.random() * 25,
      temperature: 18 + Math.random() * 15,
      ecValue: 1.2 + Math.random() * 1.5,
      pressure: 0.25 + Math.random() * 0.2,
      flowRate: 5 + Math.random() * 10,
    });
  }
  return data;
};

